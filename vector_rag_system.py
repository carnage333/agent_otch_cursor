    def _create_new_index(self):
        """–°–æ–∑–¥–∞–µ—Ç –Ω–æ–≤—ã–π –≤–µ–∫—Ç–æ—Ä–Ω—ã–π –∏–Ω–¥–µ–∫—Å"""
        # –°–æ–∑–¥–∞–µ–º —Ç–µ–∫—Å—Ç—ã –¥–ª—è —ç–º–±–µ–¥–¥–∏–Ω–≥–æ–≤
        texts = []
        for item in self.knowledge_items:
            # –û–±—ä–µ–¥–∏–Ω—è–µ–º —Ç–µ—Ä–º–∏–Ω, –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ, –ø—Ä–∏–º–µ—Ä—ã –∏ —Å–≤—è–∑–∞–Ω–Ω—ã–µ —Ç–µ—Ä–º–∏–Ω—ã
            text = f"{item.term} {item.definition}"
            if item.examples:
                text += f" {' '.join(item.examples)}"
            if item.related_terms:
                text += f" {' '.join(item.related_terms)}"
            texts.append(text)
        
        # –°–æ–∑–¥–∞–µ–º —ç–º–±–µ–¥–¥–∏–Ω–≥–∏
        print("üîÑ –°–æ–∑–¥–∞–Ω–∏–µ —ç–º–±–µ–¥–¥–∏–Ω–≥–æ–≤...")
        embeddings = self.model.encode(texts, show_progress_bar=True)
        
        # –ù–æ—Ä–º–∞–ª–∏–∑—É–µ–º —ç–º–±–µ–¥–¥–∏–Ω–≥–∏ –¥–ª—è –∫–æ—Å–∏–Ω—É—Å–Ω–æ–≥–æ —Å—Ö–æ–¥—Å—Ç–≤–∞
        embeddings = embeddings / np.linalg.norm(embeddings, axis=1, keepdims=True)
        
        # –°–æ–∑–¥–∞–µ–º FAISS –∏–Ω–¥–µ–∫—Å
        dimension = embeddings.shape[1]
        self.index = faiss.IndexFlatIP(dimension)  # Inner Product –¥–ª—è –∫–æ—Å–∏–Ω—É—Å–Ω–æ–≥–æ —Å—Ö–æ–¥—Å—Ç–≤–∞
        self.index.add(embeddings.astype('float32'))
        
        # –°–æ—Ö—Ä–∞–Ω—è–µ–º –∏–Ω–¥–µ–∫—Å –∏ —ç–º–±–µ–¥–¥–∏–Ω–≥–∏
        faiss.write_index(self.index, self.index_file)
        np.save(self.embeddings_file, embeddings)
        
        print(f"‚úÖ –í–µ–∫—Ç–æ—Ä–Ω—ã–π –∏–Ω–¥–µ–∫—Å —Å–æ–∑–¥–∞–Ω: {len(self.knowledge_items)} —ç–ª–µ–º–µ–Ω—Ç–æ–≤")
    
    def _load_existing_index(self):
        """–ó–∞–≥—Ä—É–∂–∞–µ—Ç —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–π –≤–µ–∫—Ç–æ—Ä–Ω—ã–π –∏–Ω–¥–µ–∫—Å"""
        self.index = faiss.read_index(self.index_file)
        self.embeddings = np.load(self.embeddings_file)
        print(f"‚úÖ –í–µ–∫—Ç–æ—Ä–Ω—ã–π –∏–Ω–¥–µ–∫—Å –∑–∞–≥—Ä—É–∂–µ–Ω: {len(self.knowledge_items)} —ç–ª–µ–º–µ–Ω—Ç–æ–≤")
    
    def search_knowledge(self, query: str, top_k: int = 5) -> List[KnowledgeItem]:
        """
        –°–µ–º–∞–Ω—Ç–∏—á–µ—Å–∫–∏–π –ø–æ–∏—Å–∫ –∑–Ω–∞–Ω–∏–π –ø–æ –∑–∞–ø—Ä–æ—Å—É
        
        Args:
            query: –ü–æ–∏—Å–∫–æ–≤—ã–π –∑–∞–ø—Ä–æ—Å
            top_k: –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º—ã—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤
            
        Returns:
            –°–ø–∏—Å–æ–∫ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –∑–Ω–∞–Ω–∏–π
        """
        if not self.model or not self.index:
            return []
        
        # –°–æ–∑–¥–∞–µ–º —ç–º–±–µ–¥–¥–∏–Ω–≥ –¥–ª—è –∑–∞–ø—Ä–æ—Å–∞
        query_embedding = self.model.encode([query])
        query_embedding = query_embedding / np.linalg.norm(query_embedding, axis=1, keepdims=True)
        
        # –ò—â–µ–º –ø–æ—Ö–æ–∂–∏–µ –≤–µ–∫—Ç–æ—Ä—ã
        similarities, indices = self.index.search(query_embedding.astype('float32'), top_k)
        
        # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–µ —ç–ª–µ–º–µ–Ω—Ç—ã –∑–Ω–∞–Ω–∏–π
        results = []
        for idx, similarity in zip(indices[0], similarities[0]):
            if idx < len(self.knowledge_items):
                item = self.knowledge_items[idx]
                item.embedding = self.embeddings[idx] if idx < len(self.embeddings) else None
                results.append(item)
        
        return results
    
    def enhance_report(self, report: str, question: str) -> str:
        """
        –£–ª—É—á—à–∞–µ—Ç –æ—Ç—á–µ—Ç —Å –ø–æ–º–æ—â—å—é —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω—ã—Ö –∑–Ω–∞–Ω–∏–π
        
        Args:
            report: –ò—Å—Ö–æ–¥–Ω—ã–π –æ—Ç—á–µ—Ç
            question: –í–æ–ø—Ä–æ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
            
        Returns:
            –£–ª—É—á—à–µ–Ω–Ω—ã–π –æ—Ç—á–µ—Ç —Å –∫–æ–Ω—Ç–µ–∫—Å—Ç–Ω–æ–π –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π
        """
        # –ò—â–µ–º —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω—ã–µ –∑–Ω–∞–Ω–∏—è
        relevant_items = self.search_knowledge(question, top_k=3)
        
        if not relevant_items:
            return report
        
        # –§–∏–ª—å—Ç—Ä—É–µ–º —Ç–æ–ª—å–∫–æ –≤—ã—Å–æ–∫–æ—Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω—ã–µ —ç–ª–µ–º–µ–Ω—Ç—ã (–ø–æ—Ä–æ–≥ 0.7)
        high_relevance_items = []
        for item in relevant_items:
            # –ü—Ä–æ—Å—Ç–∞—è –æ—Ü–µ–Ω–∫–∞ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç–∏ –Ω–∞ –æ—Å–Ω–æ–≤–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è —Å–ª–æ–≤
            question_words = set(question.lower().split())
            item_words = set(f"{item.term} {item.definition}".lower().split())
            common_words = question_words.intersection(item_words)
            relevance = len(common_words) / max(len(question_words), 1)
            
            if relevance > 0.3:  # –ü–æ—Ä–æ–≥ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç–∏
                high_relevance_items.append((item, relevance))
        
        if not high_relevance_items:
            return report
        
        # –°–æ—Ä—Ç–∏—Ä—É–µ–º –ø–æ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç–∏
        high_relevance_items.sort(key=lambda x: x[1], reverse=True)
        
        # –î–æ–±–∞–≤–ª—è–µ–º –∫–æ–Ω—Ç–µ–∫—Å—Ç–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é
        enhanced_report = report + "\n\n"
        enhanced_report += "üìö –ö–æ–Ω—Ç–µ–∫—Å—Ç–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è:\n"
        enhanced_report += "-" * 40 + "\n"
        
        for item, relevance in high_relevance_items:
            enhanced_report += f"üîπ {item.term}\n"
            enhanced_report += f"   {item.definition}\n"
            enhanced_report += f"üìä –†–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç—å: {relevance:.2f}\n"
            if item.examples:
                enhanced_report += f"   üí° –ü—Ä–∏–º–µ—Ä—ã: {', '.join(item.examples)}\n"
            enhanced_report += "\n"
        
        return enhanced_report
    
    def get_statistics(self) -> Dict:
        """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É RAG —Å–∏—Å—Ç–µ–º—ã"""
        categories = {}
        for item in self.knowledge_items:
            if item.category not in categories:
                categories[item.category] = 0
            categories[item.category] += 1
        
        return {
            "total_items": len(self.knowledge_items),
            "categories": categories,
            "model_name": self.model_name,
            "index_size": self.index.ntotal if self.index else 0
        }

