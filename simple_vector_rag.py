    def _build_index(self):
        """–°–æ–∑–¥–∞–µ—Ç –≤–µ–∫—Ç–æ—Ä–Ω—ã–π –∏–Ω–¥–µ–∫—Å"""
        if os.path.exists(self.index_file):
            print("üîÑ –ó–∞–≥—Ä—É–∑–∫–∞ —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ –∏–Ω–¥–µ–∫—Å–∞...")
            self.index = faiss.read_index(self.index_file)
            return
        
        print("üîÑ –°–æ–∑–¥–∞–Ω–∏–µ –≤–µ–∫—Ç–æ—Ä–Ω–æ–≥–æ –∏–Ω–¥–µ–∫—Å–∞...")
        
        # –°–æ–∑–¥–∞–µ–º —Ç–µ–∫—Å—Ç—ã –¥–ª—è —ç–º–±–µ–¥–¥–∏–Ω–≥–æ–≤
        texts = []
        for item in self.knowledge_items:
            text = f"{item['term']} {item['definition']}"
            texts.append(text)
        
        # –°–æ–∑–¥–∞–µ–º —ç–º–±–µ–¥–¥–∏–Ω–≥–∏
        embeddings = self.model.encode(texts, show_progress_bar=True)
        
        # –ù–æ—Ä–º–∞–ª–∏–∑—É–µ–º –¥–ª—è –∫–æ—Å–∏–Ω—É—Å–Ω–æ–≥–æ —Å—Ö–æ–¥—Å—Ç–≤–∞
        embeddings = embeddings / np.linalg.norm(embeddings, axis=1, keepdims=True)
        
        # –°–æ–∑–¥–∞–µ–º FAISS –∏–Ω–¥–µ–∫—Å
        dimension = embeddings.shape[1]
        self.index = faiss.IndexFlatIP(dimension)
        self.index.add(embeddings.astype('float32'))
        
        # –°–æ—Ö—Ä–∞–Ω—è–µ–º –∏–Ω–¥–µ–∫—Å
        faiss.write_index(self.index, self.index_file)
        print(f"‚úÖ –ò–Ω–¥–µ–∫—Å —Å–æ–∑–¥–∞–Ω: {len(self.knowledge_items)} —ç–ª–µ–º–µ–Ω—Ç–æ–≤")
    
    def search(self, query: str, top_k: int = 3) -> List[Dict]:
        """–°–µ–º–∞–Ω—Ç–∏—á–µ—Å–∫–∏–π –ø–æ–∏—Å–∫"""
        # –°–æ–∑–¥–∞–µ–º —ç–º–±–µ–¥–¥–∏–Ω–≥ –¥–ª—è –∑–∞–ø—Ä–æ—Å–∞
        query_embedding = self.model.encode([query])
        query_embedding = query_embedding / np.linalg.norm(query_embedding, axis=1, keepdims=True)
        
        # –ò—â–µ–º –ø–æ—Ö–æ–∂–∏–µ –≤–µ–∫—Ç–æ—Ä—ã
        similarities, indices = self.index.search(query_embedding.astype('float32'), top_k)
        
        # –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ã
        results = []
        for idx, similarity in zip(indices[0], similarities[0]):
            if idx < len(self.knowledge_items):
                item = self.knowledge_items[idx].copy()
                item['similarity'] = float(similarity)
                results.append(item)
        
        return results
    
    def enhance_report(self, report: str, question: str) -> str:
        """–£–ª—É—á—à–∞–µ—Ç –æ—Ç—á–µ—Ç —Å –ø–æ–º–æ—â—å—é –≤–µ–∫—Ç–æ—Ä–Ω–æ–≥–æ –ø–æ–∏—Å–∫–∞"""
        results = self.search(question, top_k=2)
        
        if not results:
            return report
        
        enhanced = report + "\n\nüìö –ö–æ–Ω—Ç–µ–∫—Å—Ç–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è:\n" + "-" * 40 + "\n"
        
        for item in results:
            enhanced += f"üîπ {item['term']}\n"
            enhanced += f"   {item['definition']}\n"
            enhanced += f"   üìä –†–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç—å: {item['similarity']:.2f}\n\n"
        
        return enhanced

